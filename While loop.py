#While loop
#basically a structure in python which allows us to loop through and execute a block of code multiple times.
#putting a line of code in a while loop will execute it repeatedly until a certain condition was false.

i = 1
while i <= 10: # i <= 10 is the condition that the code is restrained to
    print(i)
   # i = i + 1  <-this tells the code to add 1 to i at every increment whenever the code runs
    i += 1 #this can also be used to increment the code to tell it to keep adding 1 until the condition is met
print("The loop is finished.")
